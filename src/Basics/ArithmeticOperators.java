package Basics;

class ArithmeticOperators {
    public static void main(String[] args) {
        int x = 8;
        int y = 4;
        System.out.println(x+y);//13
        System.out.println(x-y);//3
        System.out.println(x*y);//40
        System.out.println(x/y);//1
        System.out.println(x%y);//3
    }

    
}